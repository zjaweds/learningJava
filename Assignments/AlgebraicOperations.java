public class AlgebraicOperations{
    public static void main(String args[]){
        int a=10;
        int b=3;
        System.out.println("Addition: "+ (a+b));
        System.out.println("Addition: "+ (a-b));
        System.out.println("Addition: "+ a*b);
        System.out.println("Addition: "+ a/b);
        System.out.println("Addition: "+ a%b);
    }
}